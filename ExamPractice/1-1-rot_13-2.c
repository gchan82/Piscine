#include <unistd.h>

int		main(int argc, char **argv)
{
	int i;
	i = 0;
	if(argc == 2)
	{
		if(argv[1][i] >= 'a' && argv[1][i] <= 'm')
			argv[1][i] += 13;
		else if(argv[1][i] >= 'm' && argv[1][i] <= 'z')
			argv[1][i] -= 13;
		else if(argv[1][i] >= 'A' && argv[1][i] <= 'M')
			argv[1][i] += 13;
		else if(argv[1][i] >= 'M' && argv[1][i] <= 'Z')
			argv[1][i] -= 13;
		write(1, &argv[1][i], 1);
	}
	write(1, "\n", 1);
	return (0);
}
