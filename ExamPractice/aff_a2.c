#include <unistd.h>

int		main(int ac, char **av)
{
	if (ac != 2)
		write(1, "a", 1);
	else
	{
		while (*av[1] != '\0')
		{
			if (*av[1] == 'a')
			{
				write(1, "a", 1);
				av[1]++; // not *av[1]++. change actual value of reference pointer +1.
			}
			av[1]++;
		}
	}
	write(1, "\n", 1);

	return (0);
}
