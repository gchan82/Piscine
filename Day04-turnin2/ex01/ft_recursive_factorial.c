int		ft_recursive_factorial(int nb)
{
	int i;

	i = nb;
	if (nb <= 0 || nb > 12)
		return (0);
	if (nb == 1)
		return (1);
	while (--i && nb >= 1)
		return (nb * (nb - 1));
	return (nb);
}
